# List of colors
colors = ["red", "blue", "green", "yellow", "orange"]
print("Colors:", colors)

# Access and modify
print("First color:", colors[0])
colors[1] = "purple"
print("Modified colors:", colors)

# Add and remove
colors.append("pink")
print("After append:", colors)
colors.remove("yellow")
print("After remove:", colors)

# Slicing and reverse
print("Sliced list:", colors[1:4])
colors.reverse()
print("Reversed list:", colors)
